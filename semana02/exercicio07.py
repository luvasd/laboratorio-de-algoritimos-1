### Uma empresa deseja trocar os pneus de toda frota de carros. Sabe-se que cada pneu custa R$395,40. Com isso, leia o total de carros e apresente o valor final.
##Utilize a função inpuT.####

todaFrotaDeCarros = int(input("Digite quantos carros tem a forta da sua empresa:" ))

pneu = 5

peneuCusta = 395.40

valorGasto = pneu * peneuCusta

valorFinal = valorGasto * todaFrotaDeCarros

print("Sua empresa vai gastar trocando os peneus de toda forta o valor de:", valorFinal)
